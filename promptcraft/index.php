            <ul>
                <li>Participants are given 10 short passages, each taken from various writing styles such as stories, explanations, opinions, and descriptions.</li>
                <li>The task is to identify whether each passage is written by a human or generated by an AI model.</li>
                <li>Participants must mark each passage as "AI-written" or "Human-written".</li>
                <li>Evaluation is based on the Number of Correct Responses.</li>
            </ul>
